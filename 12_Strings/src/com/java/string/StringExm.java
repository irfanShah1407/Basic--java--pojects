package com.java.string;

public class StringExm {
	public static void main(String args[]){
		String s="Hi Welcome To the World of Java";
		System.out.println("String:"+s);
		System.out.println("length of the String:"+s.length());
		System.out.println("lower case String:"+s.toLowerCase());
		System.out.println("Upper case of the String:"+s.toUpperCase());
		System.out.println("Replacing the characters:"+s.replace('h','B'));
		
		
		//similarly there are more string functions
	}

}
