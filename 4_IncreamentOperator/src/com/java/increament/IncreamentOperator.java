package com.java.increament;

public class IncreamentOperator {
	public static void main(String args[])
	{
		int a=10,b=20;
		a++;
		System.out.println("Post Increament value of a:"+a);
		++a;
		System.out.println("PreIncreament Value of a:"+a);
		b+=5;
		System.out.println("Inreament Value of b by 5:"+b);
		b-=10;
		System.out.println("Decreament Value of b by 10:"+b);
		/*similarly we can do for multiplication,division and other math operation*/
		
		
	}

}
